#include <stdio.h>
#include "function.h"

/* ****************************************
 *                                        *
 *      EINFACHE ADDITIONS FUNKTION       *
 *                                        *
 * ****************************************/

float addition(float a,float b) {
    return a + b;
}

/* ****************************************
 *                                        *
 *     EINFACHE SUBTRAKTIONS FUNKTION     *
 *                                        *
 * ****************************************/

float subtraktion(float a, float b) {
    return a - b;
}

/* ****************************************
 *                                        *
 *   EINFACHE MULTIPLIKATIONS FUNKTION    *
 *                                        *
 * ****************************************/

float multiplikation(float a, float b) {
    return a * b;
}

/* ****************************************
 *                                        *
 *      EINFACHE DIVISIONS FUNKTION       *
 *                                        *
 * ****************************************/

float division(float a, float b) {
    return a / b;
}
